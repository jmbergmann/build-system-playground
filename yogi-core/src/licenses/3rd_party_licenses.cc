const std::string k3rdPartyLicenses = ([] {
  std::string s;
  for (std::string license : licenses) {
    license.erase(0, license.find('\n') + 1);
    license.erase(std::remove(license.begin(), license.end(), '\r'),
                  license.end());
    s += license;
    s += "\n\n";
  }

  return s;
})();

